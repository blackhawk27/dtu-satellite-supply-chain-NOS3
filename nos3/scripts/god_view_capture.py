def _discover_ci_lab_ip() -> str:
    """Resolve the CI_LAB container IP via docker inspect, fall back to static.

    CI_LAB is bundled into the cFS FSW container (sc01-nos-fsw) on the
    nos3-sc01 spacecraft network, NOT on nos3-core. The legacy fallback of
    172.19.0.4 (a nos3-core address) silently mis-targeted enable commands
    away from the FSW; check sc01-nos-fsw first.
    """
    import subprocess
    for name in ("sc01-nos-fsw", "nos3-sc01-ci-lab", "nos3-ci-lab", "sc01-ci-lab"):
        try:
            result = subprocess.run(
                ["docker", "inspect", name,
                 "--format", "{{range .NetworkSettings.Networks}}{{.IPAddress}}{{end}}"],
                capture_output=True, text=True, timeout=5,
            )
            ip = result.stdout.strip().split("\n")[0]
            if ip:
                return ip
        except Exception:
            pass
    return "172.21.0.4"  # nos3-sc01 default for static-network deployments

