#!/usr/bin/env python3
"""
god_view_capture.py - Headless CCSDS Software Bus packet logger for NOS3.

Sends a TO_LAB_OUTPUT_ENABLE_CC command to CI_LAB so the telemetry output
app begins forwarding all Software Bus packets to a UDP port.  Listens on
that port, parses each packet's CCSDS primary header, and appends a JSON
line to attack_logs/cfs_god_view.json for every packet received.

Re-sends the enable command every ENABLE_INTERVAL_S seconds so it survives
CI restarts or command counter resets.

Usage (background):
    python3 scripts/god_view_capture.py &

Stop:
    pkill -f god_view_capture.py
"""

