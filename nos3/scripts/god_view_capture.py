# god_view stays the sole TO_LAB downlink destination (no contention with
# cosmos for the TO_LAB ENABLE_OUTPUT slot). cosmos still needs SAT_HELLO
# samples for the GNSS GS Responder background task, so we mirror each
# packet to a side channel. ci_launch.sh maps cosmos UDP 5013 -> host UDP
# 5113, so sending here lands in cosmos's DEBUG interface read socket.
# Override via NOS3_TLM_MIRROR_DEST="host:port" or "off" to disable.
def _resolve_mirror_dest() -> tuple[str, int] | None:
    raw = os.environ.get("NOS3_TLM_MIRROR_DEST", "127.0.0.1:5113").strip()
    if not raw or raw.lower() == "off":
        return None
    host, _, port_s = raw.partition(":")
    try:
        return host or "127.0.0.1", int(port_s) if port_s else 5113
    except ValueError:
        return None
